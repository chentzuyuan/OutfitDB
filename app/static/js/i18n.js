const I18N_KEY = 'cm_lang';

window.getLang = function () {
    // Default to English so first-time users see a familiar language;
    // the topbar lang toggle (中/EN) lets them switch to Chinese, which
    // we then persist in localStorage. Returning users keep whatever
    // they last picked.
    return localStorage.getItem(I18N_KEY) || 'en';
};

// Internal helper: returns the translation if found, or null if missing.
// Used by applyTranslations() to avoid stomping HTML defaults with raw keys
// when the cached i18n.js is older than the key being looked up.
function _tOrNull(key) {
    const lang = window.getLang();
    const v = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key])
        ?? TRANSLATIONS.zh[key]
        ?? null;
    return (typeof v === 'string' && v.length > 0) ? v : null;
}

window.t = function (key, vars) {
    let s = _tOrNull(key) ?? key;  // legacy fallback to key for direct callers
    if (vars) {
        for (const k of Object.keys(vars)) {
            s = s.split('{' + k + '}').join(vars[k]);
        }
    }
    return s;
};

window.applyTranslations = function () {
    const lang = window.getLang();
    document.documentElement.lang = lang === 'zh' ? 'zh-Hant' : 'en';
    document.querySelectorAll('[data-i18n]').forEach((el) => {
        const v = _tOrNull(el.dataset.i18n);
        // If the key isn't in the loaded i18n table (e.g. cached old version),
        // leave the element's existing text — the template default is far
        // less embarrassing than displaying "closet.select_mode" raw.
        if (v != null) el.textContent = v;
    });
    document.querySelectorAll('[data-i18n-attr]').forEach((el) => {
        const pairs = el.dataset.i18nAttr.split(',');
        for (const p of pairs) {
            const idx = p.indexOf(':');
            if (idx < 0) continue;
            const attr = p.slice(0, idx).trim();
            const key = p.slice(idx + 1).trim();
            const v = _tOrNull(key);
            if (v != null) el.setAttribute(attr, v);
        }
    });
    // Re-render JS-generated content if a re-render hook exists
    if (typeof window.onLanguageChange === 'function') {
        try { window.onLanguageChange(lang); } catch (e) { console.warn(e); }
    }
};

window.setLang = function (lang) {
    localStorage.setItem(I18N_KEY, lang);
    window.applyTranslations();
};

window.toggleLang = function () {
    window.setLang(window.getLang() === 'zh' ? 'en' : 'zh');
};

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', window.applyTranslations);
} else {
    window.applyTranslations();
}
